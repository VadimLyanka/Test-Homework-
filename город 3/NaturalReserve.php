          <p>Кодры (Кодру, молд. Codrii) — самый старый научный заповедник Молдавии. Представляет собой холмистые леса с максимальными высотами до 380 м, пересечён долинами, оврагами, гыртопами.

Уже с XIX века леса в Молдавии заметно поредели, их вытесняют виноградники, яблоневые сады, поля сельхозкультур. Почти вся территория Молдавии возделана, естественная растительность сохранилась менее, чем на 10% территории. Для сохранения и восстановления лесов на возвышенности Кодры (наиболее лесной регион Молдавии), 27 сентября 1971 года был создан заповедник с одноимённым названием.</p>
