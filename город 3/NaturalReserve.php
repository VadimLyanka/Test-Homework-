    <h2>География</h2>
        <article>
          <p>Заповедник «Кодры» расположен у села Лозово Страшенского района Республики Молдова, примерно в 50 км от Кишинёва[3]. Наивысшая точка заповедника (382,5 м на уровнем моря) находится на водоразделе бассейнов рек Когильник и Бык. Водораздел рек Ботна и Бык проходит примерно посередине заповедника.

Первоначальная площадь была 2740 га, включая строго охраняемую зону площадью 732 га. В 1975 году постановлением правительства площадь расширилась до 5009 га[5].

Заповедник представляет собой единый лесной массив. С юга, запада и севера он окружён сельскохозяйственными угодьями, на северо-востоке граничит с лесами Страшенского лесхоза[4]. Территория заповедника разделена на три функциональные зоны</p>
        </article>

        <h2>Флора</h2>
        <article>
          <p>Флора заповедника представлена более чем 1000 видов растений (67 видов лишайников, 334 вида грибов, 69 видов мха и др.). Основные виды деревьев в «Кодрах» — дуб и бук. Наиболее распространены дубравы, состоящие из дуба скального, - около 40 % площади лесов заповедника (1970 га). Леса с преобладанием дуба черешчатого занимают около 540 га, в нижней части склонов и по долинам ручьёв. Леса из бука европейского сохранились всего на 22 га. Его небольшие участки встречаются на высотах 220-300 м, на крутых склонах оврагов. 411 га занимают грабовые леса, образовавшиеся на месте старых вырубок. Леса с преобладанием ясеня обыкновенного занимают около 1880 га, около 350 га - леса искусственного происхождения. В последние десятилетия идёт процесс усыхания всех видов дуба.

На территории заповедника находится живописная зона плавней и действует «Музей Природы». В заповеднике произрастают около 90 видов редких растений. В лесу можно встретить такие растения как пушица, жабник, болотница. Хорошо развиты кустарники и травяной покров: кизил, бересклет европейский, калина, сныть, черемша. В Красную книгу Молдавии входят пушица широколистная, лунник оживающий, вязель изящный (Coronilla elegans)[4]. Только на территории заповедника в Молдавии известны: дремлик болотный, ятрышник болотный, зверобой четырёхкрылый.</p>
        </article>


        <h2>Фауна</h2>
        <article>
          <p>Фауна заповедника «Кодры» представляет почти полностью фауну широколиственных лесов центральной и западной Европы и включают около 52 видов млекопитающих, 151 вид птиц, 8 видов рептилий, 10 видов земноводных и более 8000 видов насекомых. Из копытных в заповеднике обитают: дикий кабан, европейская косуля, европейский благородный олень, пятнистый олень. 10 видов птиц занесены в Красную книгу Молдавии, включая черного аиста. Это единственный заповедник в стране, где регулярно производились исследования беспозвоночных. Благодаря заповеднику удалось сохранить исчезающие виды животных. Так, например, лесных куниц, занесенных в Красную книгу Молдавии, на территории Молдавии можно встретить только в Кодрах.

В заповеднике обитают также: каменная куница, обыкновенный слепыш, белобрюхая белозубка, сони (полчок, орешниковая, лесная), лисица, барсук, ласка. Из летучих мышей водятся малая вечерница, рыжая вечерница, водяная ночница. Также здесь можно встретить европейского лесного кота, который занесен в международную Красную книгу и Красную книгу Молдавии. В заповеднике обитают 2 подвида белки: обыкновенная и карпатская. Заяц-русак раньше был многочислен в Молдавии, но из-за интенсивного ведения сельского хозяйства, применения химических средств защиты растений его численность упала. Встречаются редкие для Молдавии виды: черный дятел - желна, сирийский дятел, канареечный вьюрок, ястреб-тетеревятник, ястреб-перепелятник, канюк. В начале 1970-х годов гнездился филин, но его последняя встреча на заповедной территории зарегистрирована в 1977 году. Из сов чаще всего встречается обыкновенная неясыть. В дубравах гнездится ворон (7-11 пар). Из ядовитых змей обитает только обыкновенная гадюка.

Научные исследования в заповеднике проводят Академия наук Молдовы, кафедра ботаники Государственного университета Молдовы, Тираспольский государственный университет.</p>
